function maiorDeTres (n1, n2, n3){
    if(n1 > n2 && n1>n3){
        console.log(`O numero ${n1}, é o maior dentre os três`)
    }
    else if(n2 > n3 && n1){
        console.log(`O numero ${n2}, é o maior dentre os três`)
    } else{
        console.log(`O numero ${n3}, é o maior dentre os três`)
    }
}

